class PatchEmbed(nn.Module):


    def __init__(self, img_size=224, patch_size=4, in_chans=3, embed_dim=96, norm_layer=None):
        super().__init__()
        img_size = to_2tuple(img_size)
        patch_size = to_2tuple(patch_size)
        patches_resolution = [img_size[0] // patch_size[0], img_size[1] // patch_size[1]]
        self.img_size = img_size
        self.patch_size = patch_size
        self.patches_resolution = patches_resolution
        self.num_patches = patches_resolution[0] * patches_resolution[1]

        self.in_chans = in_chans
        self.embed_dim = embed_dim

        self.proj = nn.Conv2d(in_chans, embed_dim, kernel_size=patch_size, stride=patch_size)
        if norm_layer is not None:
            self.norm = norm_layer(embed_dim)
        else:
            self.norm = None

    def forward(self, x):
        B, C, H, W = x.shape
        # FIXME look at relaxing size constraints
        assert H == self.img_size[0] and W == self.img_size[1], \
            f"Input image size ({H}*{W}) doesn't match model ({self.img_size[0]}*{self.img_size[1]})."
        x = self.proj(x).flatten(2).transpose(1, 2)  # B Ph*Pw C
        if self.norm is not None:
            x = self.norm(x)
        return x

    def flops(self):
        Ho, Wo = self.patches_resolution
        flops = Ho * Wo * self.embed_dim * self.in_chans * (self.patch_size[0] * self.patch_size[1])
        if self.norm is not None:
            flops += Ho * Wo * self.embed_dim
        return flops

class GatedConv(nn.Conv2d):
    def __init__(self, in_channels, out_channels):
        super().__init__(in_channels, out_channels, 1, bias=False)
        self.attention = nn.Sequential(
            nn.BatchNorm2d(in_channels+1),
            nn.Conv2d(in_channels +1, in_channels +1, 1),
            nn.ReLU(),
            nn.Conv2d(in_channels +1, 1, 1),
            nn.BatchNorm2d(1),
            nn.Sigmoid()
        )
    def _init_weights(self, m):
        if isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()
        elif isinstance(m, nn.BatchNorm2d):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
    def forward(self,x,y):
        attention = self.attention(torch.cat((x, y), dim=1))
        out = F.conv2d(x * (attention + 1), self.weight)
        return out
class ShapeStream(nn.Module):
    def __init__(self, dim):
        super().__init__()
        self.res1_conv = nn.Sequential(nn.Conv2d(dim , dim, 1, 1, 0),nn.BatchNorm2d(dim),nn.ReLU(True))
        self.res2_conv = nn.Sequential(nn.Conv2d(dim*2 , dim*2, 1, 1, 0),nn.BatchNorm2d(dim*2),nn.ReLU(True))
        self.res3_conv = nn.Sequential(nn.Conv2d(dim*4 , dim*4, 1, 1, 0),nn.BatchNorm2d(dim*4),nn.ReLU(True))
        self.gate1 = GatedConv(dim, 1)
        self.gate2 = GatedConv(dim*2, 1)
        self.gate3 = GatedConv(dim*4, 1)
        self.down1 = nn.Conv2d(1,1,3,2,1)
        self.down2 = nn.Conv2d(1,1,3,2,1)
        self.down_x4=nn.Conv2d(3,1,3,4,1)
    def _init_weights(self, m):
        if isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()
        elif isinstance(m, nn.BatchNorm2d):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
    def forward(self,  res1, res2, res3, grad):
        res1 = self.res1_conv(res1)
        res2 = self.res2_conv(res2)
        res3 = self.res3_conv(res3)
        grad = self.down_x4(grad)
        gate1 = self.gate1(res1, grad)
        dgrad1 = self.down1(gate1)
        gate2 = self.gate2(res2, dgrad1)
        dgrad2 = self.down2(gate2)
        gate3 = self.gate3(res3, dgrad2)
        gate1 = torch.sigmoid(gate1)
        gate2 = torch.sigmoid(gate2)
        gate3 = torch.sigmoid(gate3)
        gate1 = gate1.flatten(2).transpose(1, 2)
        gate2 = gate2.flatten(2).transpose(1, 2)
        gate3 = gate3.flatten(2).transpose(1, 2)
        return gate1,gate2,gate3
